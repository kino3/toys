-- 4,000,000

-- sum of fibonacci
-- 1,2,3,5,8,...
-- 1,3,6,11,19,...

-- 1 = 1
-- 3 = 1+2 = 1+(1+1)
-- 6 = 3+3 = 3+(1+2)
-- 11= 6+5 = 6+(2+3)
-- 19=11+8 =11+(3+5)=6+(2+3)+(3+5)=3+(1+2)+(2+3)+(3+5)
-- = 1+(1+1)+(1+2)+(2+3)+(3+5)
-- = 1*4+2*2+3*2+5*1
fib = 1 : 1 : zipWith (+) fib (tail fib)
main = do
  print (sum [ x | x <- takeWhile (<= 4000000) fib, even x])
